#include "libft.h"

int	ft_atoi(const char *str)
{
	int				s;
	long int		n;
	unsigned int	i;	

	i = 0;
	s = 1;
	while (str[i] != '\0' && (str[i] == ' '
			|| (str[i] >= '\t' && str[i] <= '\r')))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	n = 0;
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		n = n * 10 + (str[i] - '0');
		i++;
	}
	return (n * s);
}
